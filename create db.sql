CREATE DATABASE db_Project 
GO
USE db_Project; 
GO
CREATE SCHEMA production 
GO
CREATE SCHEMA sales 
GO
CREATE TABLE production.Brands( 
	BrandId INT PRIMARY KEY IDENTITY,
	Name NVARCHAR(250)
)
GO
CREATE TABLE production.Categories( 
	CategoryId INT PRIMARY KEY IDENTITY,
	Name NVARCHAR(250)
)
GO
CREATE TABLE production.Products(
	ProductId INT PRIMARY KEY IDENTITY,
	Name NVARCHAR(250),
	Price FLOAT,
	Year DATETIME,
	BrandId INT,
	CategoryId INT,
	CONSTRAINT FK_Brands FOREIGN KEY(BrandId)
	REFERENCES production.Brands(BrandId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_Categories FOREIGN KEY(CategoryId)
	REFERENCES production.Categories(CategoryId)
	ON DELETE CASCADE
	ON UPDATE CASCADE
)
GO
CREATE TABLE sales.Stores(
	StoreId VARCHAR(50) PRIMARY KEY,
	Name NVARCHAR(250),
	Street NVARCHAR(250),
	District NVARCHAR(250),
	City NVARCHAR(250),
	Email VARCHAR(250) UNIQUE,
	PhoneNumber VARCHAR(20) UNIQUE,
	CONSTRAINT UQ_EmailAndPhone UNIQUE(Email, PhoneNumber)
)
GO
CREATE TABLE production.Stocks(
	StockId INT PRIMARY KEY IDENTITY,
	Quantity INT,
	ProductId INT,
	StoreId VARCHAR(50),
	Sold INT DEFAULT 0,
	CONSTRAINT FK_Stores FOREIGN KEY(StoreId)
	REFERENCES sales.Stores(StoreId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_Products FOREIGN KEY(ProductId)
	REFERENCES production.Products(ProductId)
	ON DELETE CASCADE
	ON UPDATE CASCADE
)
GO
CREATE TABLE sales.Discounts(
	DiscountId INT PRIMARY KEY IDENTITY,
	DiscountName NVARCHAR(250),
	StartAt DATETIME,
	EndAt DATETIME,
	DiscountType NVARCHAR(250),
	DiscountPercent FLOAT,
	DiscountAmount FLOAT
)
GO
CREATE TABLE sales.Customers(
	CustomerId INT PRIMARY KEY IDENTITY,
	FirstName NVARCHAR(50),
	LastName NVARCHAR(50),
	MidName NVARCHAR(50),
	Street NVARCHAR(250),
	District NVARCHAR(250),
	City NVARCHAR(250),
	Email VARCHAR(250) UNIQUE,
	PhoneNumber VARCHAR(20) UNIQUE,
	CONSTRAINT UQ_CustomerEmailPhone UNIQUE(Email, PhoneNumber)
)
GO

CREATE TABLE sales.Staffs(
	StaffId INT PRIMARY KEY IDENTITY,
	FirstName NVARCHAR(50),
	LastName NVARCHAR(50),
	MidName NVARCHAR(50),
	[Status] NVARCHAR(50),
	Email VARCHAR(250) UNIQUE,
	PhoneNumber VARCHAR(20) UNIQUE,
	ManagerId INT,
	StoreId VARCHAR(50),
	CONSTRAINT FK_Stores FOREIGN KEY(StoreId)
	REFERENCES sales.Stores(StoreId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_Staffs FOREIGN KEY(ManagerId)
	REFERENCES sales.Staffs(StaffId)
	ON DELETE NO ACTION
	ON UPDATE NO ACTION
)
GO
CREATE TABLE sales.Orders(
	OrderId INT PRIMARY KEY IDENTITY,
	CreatedDate DATETIME,
	RequireDate DATETIME,
	ShippedDate DATETIME,
	OrderStatus NVARCHAR(250),
	CustomerId INT,
	StaffId INT,
	StoreId VARCHAR(50),
	CONSTRAINT FK_OrdersStores FOREIGN KEY(StoreId)
	REFERENCES sales.Stores(StoreId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_OrdersStaffs FOREIGN KEY(StaffId)
	REFERENCES sales.Staffs(StaffId)
	ON DELETE NO ACTION
	ON UPDATE NO ACTION,
	CONSTRAINT FK_Customers FOREIGN KEY(CustomerId)
	REFERENCES sales.Customers(CustomerId)
	ON DELETE CASCADE
	ON UPDATE CASCADE
)
GO
CREATE TABLE sales.OrderItems(
	OrderItemId INT PRIMARY KEY IDENTITY,
	Quantity INT,
	Price FLOAT,
	OrderId INT,
	ProductId INT,
	DiscountId INT,
	CONSTRAINT FK_Orders FOREIGN KEY(OrderId)
	REFERENCES sales.Orders(OrderId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_Products FOREIGN KEY(ProductId)
	REFERENCES production.Products(ProductId)
	ON DELETE CASCADE
	ON UPDATE CASCADE,
	CONSTRAINT FK_Discounts FOREIGN KEY(DiscountId)
	REFERENCES sales.Discounts(DiscountId)
	ON DELETE CASCADE
	ON UPDATE CASCADE
)
GO
